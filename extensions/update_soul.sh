### 🧠 Rule 1 — Memory First Policy

> **Завжди** викликай `memory_search` перед прийняттям будь-якого архітектурного
> рішення.

Ти маєш доступ до семантичної пам'яті MemPlace. Перш ніж проєктувати,
рефакторити чи обирати підхід:

1. Сформулюй пошуковий запит, що описує проблему або рішення.
2. Виклич інструмент `memory_search(query: "<опис>", topK: 5)`.
3. Проаналізуй результати: минулі патерни, помилки, рішення.
4. **Тільки після цього** формулюй свій план.

Якщо `memory_search` повертає релевантні спогади — **обов'язково** цитуй їх
у відповіді або файлі плану, щоб забезпечити прозорість рішення.

```
ЦИКЛ: Питання → memory_search → Аналіз → Рішення → memory_add (зберегти нове)
```

Після завершення значного рішення або відкриття — зберігай його:
```
memory_add(content: "<summary>", tags: ["architecture", "lesson-learned"])
```

---

### 📋 Rule 2 — Spec-Driven Development

> **Заборонено** писати функціональний код без наявності плану.

Перед написанням будь-якого функціонального коду переконайся, що існують:

- **`.planning/PLAN.md`** — загальний план проєкту з декомпозицією на фази.
- **`.planning/CONTEXT.md`** — контекст поточного стану, обмеження, стек.

**Алгоритм перед кодуванням:**

1. Перевір наявність `.planning/PLAN.md`. Якщо відсутній — створи через
   `gsd_plan_phase`.
2. Прочитай відповідну фазу. Переконайся, що розумієш scope кожного кроку.
3. Тільки після підтвердження наявності плану — починай імплементацію.

Виняток: hotfix-и для критичних помилок продакшену можуть виконуватися негайно,
але МУСЯТЬ бути задокументовані в `.planning/STATE.md` ретроспективно протягом
тієї ж сесії.

