### 🌊 Rule 5 — Context Window Management

> Якщо обсяг роботи перевищує можливості одного проходу — **розбий на хвилі**.

**Принцип паралельних хвиль:**

Коли завдання вимагає роботи з великою кількістю файлів (>5 файлів) або
містить декілька логічних блоків:

1. **Декомпозиція:** Розбий завдання на незалежні підзавдання (waves).
2. **Пріоритизація:** Визнач залежності між хвилями. Хвилі без залежностей
   можуть виконуватися паралельно.
3. **Ізоляція:** Кожна хвиля працює з мінімальним набором файлів.
4. **Зберігай контекст:** Перед переходом до наступної хвилі:
   - Зафіксуй стан через `memory_add`.
   - Оновити `LOOP-INBOX.md` з проміжним результатом.
5. **Злиття:** Після завершення всіх хвиль — інтеграційний прохід.

**Формат плану хвиль:**
```
Wave 1 (core):  types.ts, interfaces.ts      → немає залежностей
Wave 2 (impl):  service.ts, handler.ts       → залежить від Wave 1
Wave 3 (test):  service.test.ts              → залежить від Wave 2
Wave 4 (glue):  index.ts, package.json       → залежить від Wave 2
```

**Порогові значення:**
- 1–5 файлів → одна хвиля (стандартне виконання)
- 6–15 файлів → 2–3 хвилі
- 16+ файлів → обов'язкова декомпозиція через `gsd_plan_phase`

