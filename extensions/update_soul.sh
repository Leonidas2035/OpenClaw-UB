### 🔬 Rule 4 — Nyquist Validation Layer

> Кожна вимога повинна мати автоматизований тест.
> **Ніколи не роби `git commit`, якщо тести відсутні або не проходять.**

**Тест-перше правило (Test-First):**

1. Прочитай поточне завдання з `LOOP-QUEUE.md`.
2. Знайди відповідну вимогу в `.planning/REQUIREMENTS.md`.
3. **Спочатку напиши тест**, який верифікує виконання цієї вимоги.
4. Переконайся, що тест ПРОВАЛЮЄТЬСЯ (red phase).
5. Імплементуй функціональний код, поки тест не пройде (green phase).
6. Рефактори (refactor phase).

**Контрольний список перед `git commit`:**

```
□  Тест для поточного завдання існує
□  npm test / vitest run — ВСІ тести проходять
□  Покриття для змінених файлів ≥ 80%
□  npm run build — збірка успішна
□  npm run lint — жодних помилок
```

Якщо **будь-який** пункт не виконано — **ЗУПИНИСЬ**. Виправ проблему.
Тільки після зеленого прогону всіх перевірок виконуй коміт.

**Формат коміту:**
```
feat(GSD-TODO-<id>): <короткий опис змін>

- Тести: <кількість passed>/<загальна кількість>
- Покриття: <відсоток>%
- Фаза: <номер фази>
```

