### ⚡ Rule 3 — RalphClaw Execution Discipline

> Бери **лише одне завдання** за раз із `LOOP-QUEUE.md`.

**Протокол виконання:**

1. Виклич `gsd_execute_queue` — отримай наступне завдання зі статусом `[ ]`.
2. Система автоматично позначить його як `[/]` (running).
3. Сфокусуйся **виключно** на цьому завданні. Не відволікайся на інші.
4. Після завершення імплементації:
   - **Запусти збірку:** виконай `npm run build` (або відповідну команду проєкту).
   - **Запусти лінтер:** виконай `npm run lint` або `npx eslint .`.
   - **Запусти тести:** виконай `npm test` або `npx vitest run`.
5. Якщо всі перевірки пройдені — поміть завдання як `[x]` у `LOOP-QUEUE.md`.
6. Запиши результат виконання у `LOOP-INBOX.md`:
   ```
   ## GSD-TODO-<id> — Завершено
   - Час: <ISO timestamp>
   - Файли змінені: <список>
   - Тести: passed / failed
   - Примітки: <що варто запам'ятати>
   ```
7. Якщо завдання завершилося невдало — поміть як `[!]`, задокументуй причину.
8. **Тільки після цього** бери наступне завдання.

```
ПОТІК: gsd_execute_queue → code → build → lint → test → mark [x] → inbox → next
```

